u64 fb_addr = 0;
int hor_res = 0;
int ver_res = 0;
int p_scanln = 0;
int p_format = RGB;
int size = 0;

void vga_init(u64 fb_base, int hres, int vres, int p_scan, int p_mode) {
    fb_addr = fb_base;
    size = hres * vres;
    hor_res = hres;
    p_scanln = p_scan;
    ver_res = vres;
	p_format = p_mode;
}

void draw_pixel(int x, int y, u32 color) {
    u32 *vga = (u32 *)fb_addr;
    int addr = (y * p_scanln) + x;
    if (addr <= size) {
		if (p_format == BGR) {
			u32 BgrColor = (color & MaskBlue) << 16;
			BgrColor += color & MaskGreen;
			BgrColor += (color & MaskRed) >> 16; 
			vga[addr] = BgrColor;
		} else if (p_format == RGB) {
			vga[addr] = color;
		}
    }
}

void draw_16color(int x, int y, int color_index) {
    u32 *vga = (u32 *)fb_addr;
    int addr = (y * p_scanln) + x;
    u32 color = vga16_color[color_index];
    if (addr <= size) {
		if (p_format == BGR) {
			u32 BgrColor = (color & MaskBlue) << 16;
			BgrColor += color & MaskGreen;
			BgrColor += (color & MaskRed) >> 16;
			vga[addr] = BgrColor; 
		} else if (p_format == RGB) {
			vga[addr] = color;
		}
    }
}

